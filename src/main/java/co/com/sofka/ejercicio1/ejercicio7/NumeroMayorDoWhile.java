package co.com.sofka.ejercicio1.ejercicio7;

public class NumeroMayorDoWhile {
    private double numero;

    public NumeroMayorDoWhile() {
        this.numero = numero;
    }

    public double getNumero() {
        return numero;
    }

    public void setNumero(double numero) {
        this.numero = numero;
    }
    public boolean comparador(){
        if(this.numero > 0 || this.numero ==0) return true;
        else return false;
    }
}
